-module(fpl2_5).
-export([task/1]).

% Головна функція викликає допоміжну з порожнім акумулятором
task(List) -> 
    reverse(List, []).

% Якщо список порожній, повертаємо акумулятор
reverse([], Acc) -> 
    Acc;
% Переносимо початок списку до акумулятора
reverse([H | T], Acc) -> 
    reverse(T, [H | Acc]).