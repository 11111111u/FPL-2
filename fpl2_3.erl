-module(fpl2_3).
-export([task/2]).

% Знайти N-й елемент списку:
task([H | _], 1) -> 
    % Повертає елемент, якщо N = 1
    H; 
task([_ | T], N) when N > 1 -> 
    % Рекурсивно зменшує N та обходить список
    task(T, N - 1); 
task(_, _) -> 
    % Якщо N недопустиме або список закінчився код повертає undefined
    undefined. 